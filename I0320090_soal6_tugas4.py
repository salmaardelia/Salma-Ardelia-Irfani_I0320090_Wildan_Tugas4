#Soal nomor 6 tugas 4
a = 4
b = 11
print('Nilai :',a,' , binary :',format(a,'08b'))
print('Nilai :',b,' , binary :',format(b,'08b'))

#a. 4 | 11
c = a | b
print('Keluaran dari operator bitwise 4 | 11')
print('Nilai :',c,' , binary :',format(c,'08b'))

#b. 4 >> 11
c = a >> b
print('Keluaran dari operator bitwise 4 >> 11')
print('Nilai :',c,' , binary :',format(c,'08b'))

#c. 4 ^ 11
c = a ^ b
print('Keluaran dari operator bitwise 4 ^ 11')
print('Nilai :',c,' , binary :',format(c,'08b'))

#d. ~4
c = ~a
print('Keluaran dari operator bitwise ~4')
print('Nilai :',c,' , binary :',format(c,'08b'))

#e. 11 & 4
c = b & a
print('Keluaran dari operator bitwise 4 & 11')
print('Nilai :',c,' , binary :',format(c,'08b'))